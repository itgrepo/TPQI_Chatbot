def get_relevant_docs(query_text, limit=3):
    try:
        # 1. ‡πÅ‡∏õ‡∏•‡∏á‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏° User ‡πÄ‡∏õ‡πá‡∏ô Vector
        query_vector = embeddings.embed_query(query_text)

        # 2. ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡πÉ‡∏ô Firestore (Vector Search)
        # ‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏´‡∏ï‡∏∏: ‡∏´‡∏≤‡∏Å‡∏£‡∏±‡∏ô‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡πÅ‡∏£‡∏Å‡πÅ‡∏•‡πâ‡∏ß Error ‡∏ï‡πâ‡∏≠‡∏á‡πÑ‡∏õ‡∏Å‡∏î‡∏™‡∏£‡πâ‡∏≤‡∏á Index ‡πÉ‡∏ô Google Cloud Console ‡∏Å‡πà‡∏≠‡∏ô
        coll = db.collection(COLLECTION_NAME)
        vector_query = coll.find_nearest(
            vector_field="embedding_field", # ‡∏ä‡∏∑‡πà‡∏≠ Field ‡∏ó‡∏µ‡πà‡πÄ‡∏Å‡πá‡∏ö Vector ‡πÉ‡∏ô Firestore
            query_vector=Vector(query_vector),
            distance_measure=firestore.DistanceMeasure.COSINE,
            limit=limit
        )
        
        docs = vector_query.get()
        
        # 3. ‡∏£‡∏ß‡∏ö‡∏£‡∏ß‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• (Context) ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏™‡πà‡∏á‡πÉ‡∏´‡πâ LLM
        context_text = ""
        for doc in docs:
            data = doc.to_dict()
            content = data.get('content', '')
            # ‡∏î‡∏∂‡∏á Metadata ‡πÅ‡∏´‡∏•‡πà‡∏á‡∏ó‡∏µ‡πà‡∏°‡∏≤ (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ)
            source = data.get('metadata', {}).get('source', 'Unknown Document')
            context_text += f"\n[‡∏ó‡∏µ‡πà‡∏°‡∏≤: {source}]\n{content}\n---"
            
        return context_text if context_text else "‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•"
        
    except Exception as e:
        st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•: {e}")
        return ""

